package sundayTestApartment;

public class Playground {
	

	int bat;
	int ball;
	boolean ground;

	public Playground(int bat, int ball, boolean ground) {
		super();
		this.bat = bat;
		this.ball = ball;
		this.ground = ground;
	}
	@Override
	public String toString() {
		return "Playground [bat=" + bat + ", ball=" + ball + ", ground=" + ground + "]";
	}

}
