package oneSoftTest3;

public class SwitchTest15 {
 public static void main(String[] args) {
	int val = 1;
	switch (val) {
	case 1 : System.out.println("1,2,3,4,5");
	break;
	case 2 : System.out.println("2,3,4,5");
	break;
	case 3 : System.out.println("3,4,5");
	break;
	case 4 : System.out.println("4,5");
	break;
	case 5 : System.out.println("5");
	break;
	default :
		System.out.println("invalid");
	
	
	
	}
	 
	 
	 
}
}
