package oneSoftTest3;

public class SwitchTest2 {
	public static void main(String[] args) {
		String num = "one";

		switch (num) {

		case "one":
			System.out.println(1);
			break;
		case "two":
			System.out.println(2);
			break;
		case "three":
			System.out.println(3);
			break;
		case "four":
			System.out.println(4);
			break;
		case "five":
			System.out.println(5);
			break;
		}
	}

}
